-- Total revenue generated by each medicine
SELECT m.name AS Medicine, SUM(s.total_price) AS Total_Revenue
FROM Sales s
JOIN Medicines m ON s.medicine_id = m.medicine_id
GROUP BY m.name;

-- The supplier who supplied the highest total quantity of medicines
SELECT s.name AS Supplier, SUM(p.quantity) AS Total_Quantity
FROM Purchases p
JOIN Suppliers s ON p.supplier_id = s.supplier_id
GROUP BY s.name
ORDER BY Total_Quantity DESC
LIMIT 1;


-- Total number of medicines sold for each medicine type/category
SELECT m.type AS Medicine_Type, SUM(s.quantity) AS Total_Sold
FROM Sales s
JOIN Medicines m ON s.medicine_id = m.medicine_id
GROUP BY m.type;


-- Medicines whose stock quantity is below the average stock of all medicines
SELECT name, stock_quantity
FROM Medicines
WHERE stock_quantity < (SELECT AVG(stock_quantity) FROM Medicines);

-- Medicines that have NEVER been sold
SELECT m.name
FROM Medicines m
WHERE m.medicine_id NOT IN (SELECT DISTINCT medicine_id FROM Sales);


-- Suppliers who have supplied more than 500 units of medicines in total
SELECT s.name AS Supplier, SUM(p.quantity) AS Total_Supplied
FROM Purchases p
JOIN Suppliers s ON p.supplier_id = s.supplier_id
GROUP BY s.name
HAVING SUM(p.quantity) > 500;

-- Medicine types whose average price is greater than 100
SELECT type, AVG(unit_price) AS Avg_Price
FROM Medicines
GROUP BY type
HAVING AVG(unit_price) > 100;


-- Complete purchase details including medicine name and supplier name
SELECT p.purchase_id, m.name AS Medicine, s.name AS Supplier, p.quantity, p.purchase_date
FROM Purchases p
JOIN Medicines m ON p.medicine_id = m.medicine_id
JOIN Suppliers s ON p.supplier_id = s.supplier_id;


-- Detailed sales records along with medicine information (name + type)
SELECT s.sale_id, m.name AS Medicine, m.type, s.quantity, s.sale_date, s.total_price
FROM Sales s
JOIN Medicines m ON s.medicine_id = m.medicine_id;


-- Medicines that are going to expire within the next 90 days
SELECT name, expiry_date
FROM Medicines
WHERE expiry_date BETWEEN CURDATE() AND DATE_ADD(CURDATE(), INTERVAL 90 DAY);


-- Total number of sales transactions that happened in the last 30 days (including today)
SELECT COUNT(*) AS Sales_Last_30_Days
FROM Sales
WHERE sale_date >= DATE_SUB(CURDATE(), INTERVAL 30 DAY);


