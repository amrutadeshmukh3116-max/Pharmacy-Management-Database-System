    m.name,
    m.stock_quantity - COALESCE(SUM(sa.quantity), 0) AS remaining_stock
FROM MEDICINES m
LEFT JOIN sales sa
ON m.medicine_id = sa.medicine_id
GROUP BY m.name, m.stock_quantity;

