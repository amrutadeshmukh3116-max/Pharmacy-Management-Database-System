SELECT 
    name,
    revenue,
    RANK() OVER (ORDER BY revenue DESC) AS sales_rank
FROM (
    SELECT 
        m.name,
        SUM(s.total_price) AS revenue
    FROM sales s
    JOIN MEDICINES m
    ON s.medicine_id = m.MEDICINE_ID
    GROUP BY m.name
) t;

-- VIEW
CREATE VIEW pharmacy_dashboard AS
SELECT 
    m.name,
    SUM(s.quantity) AS total_sold,
    SUM(s.total_price) AS revenue
FROM MEDICINES m
LEFT JOIN sales s
ON m.MEDICINE_ID = s.medicine_id
GROUP BY m.name;
